// spells us two dimensional types
package types

// type of a Point
type Point struct {
	X float64
	Y float64
}

// type of triangle
type Triangle struct {
	sideA  float64
	sideB  float64
	sideC  float64
	height float64
}

// type of square
type Square struct {
	length float64
}

// type of rectangle
type Rectangle struct {
	length float64
	width  float64
}

